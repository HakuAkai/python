# Divisão inteira = // (retorna um número inteiro)
# Módulo = % (resto da divisão)
# Exponenciação = **

dia = 28
mes = 10
ano = 2005
print(dia,"/", mes,"/",ano)

n1 = dia + mes + ano
print(n1)

n1 //= 4
n1 //= 3
n1 //= 3
n1 //= 2
n1 //= 2

nr = n1 // 2
print(not nr >= 10)
print(nr)

print("O resultado é maior que 10? ", nr > 10)
print("O resultado é menor que 10? ",nr < 10)
print("O resultado é menor ou igual a 5? ",nr <= 5)
print("O resultado é igual a 7? ",nr == 7)

